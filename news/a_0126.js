  <p>
  Back in 1991, Oliver Mayer (aka BSC) wrote 
  <a href="https://www.grimware.org/doku.php/documentations/software/soundtrakker/start" target="_blank">Soundtrakker</a>,
  a cool music tracker for the Amstrad CPC. It was quite popular in the demoscene of that time. Now, thirty years later,
  I made a little JavaScript player for music modules written in Soundtrakker 1.x to listen to songs from authors like
  BSC, Kangaroo, Tom&amp;Jerry, and others, directly in your web browser. I did not (yet) include a full-fledged emulator
  for the AY-3-8910 soundchip of the CPC, but just used the standard oscillators of the Web Audio API, so it will not
  sound as cool as the real hardware, and sometimes be also a little bit out of tune. So, in such cases it is (mostly)
  not the song authors who are to blame, but my program. For most songs it seems to work quite OK, though.
</p>
<p>
